package nexsoft.day6_function;

public class tugasDay6_4 {

	public static void main(String[] args) {
		perkalian();
		pengurangan();
	}

	static void perkalian() { // method parameter
		System.out.println("Perkalian A * B = 24");
	}

	static void pengurangan() { // method parameter
		System.out.println("Pengurangan A - B = 5");
	}

}
